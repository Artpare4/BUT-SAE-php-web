<?php

namespace Entity;

class Image
{
    private int $id;
    private string $jpeg;

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @return string
     */
    public function getJpeg(): string
    {
        return $this->jpeg;
    }

    /**
     * @param int $id
     * @return Image
     */
    public function setId(int $id): Image
    {
        $this->id = $id;
        return $this;
    }

    /**
     * @param string $jpeg
     * @return Image
     */
    public function setJpeg(string $jpeg): Image
    {
        $this->jpeg = $jpeg;
        return $this;
    }
}